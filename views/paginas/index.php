            <p>
            <strong>Bienvenidos a SalmonGuard:</strong> Protegiendo a Nuestros Valiosos Salmónidos, En <strong>SalmonGuard</strong> estamos comprometidos en abordar el desafiante Síndrome Rickettsial del Salmón para mantener a los salmones saludables y seguros.
            <br> 
            Para aquellos que aún no están familiarizados con nosotros, aquí presentamos una breve visión general de nuestro enfoque y misión:
            <br> 
            <strong>¿Qué es el Síndrome Rickettsial del Salmón?</strong> El Síndrome Rickettsial del Salmón es una enfermedad que amenaza a nuestros queridos salmones. 
            Esta enfermedad es causada por bacterias del género Rickettsiales que pueden invadir y parasitar las células de los tejidos de los salmones, causando daño celular y afectando su salud en general.  
            Por lo que hemos creado <strong>SalmonGuard</strong> y hemos asumido la tarea de desarrollar una avanzada <strong>API</strong> de reconocimiento de enfermedades en salmones a partir de videos de prueba.
            <br>
            Lo que hace especial a nuestra solución es que empleamos el poder del aprendizaje automático (machine learning) para identificar salmones.
            </p>
